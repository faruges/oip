            Sujetos Obligados de la Ciudad de México, la Secretaría de Administración y Finanzas, en su función de responsable del tratamiento de datos
            personales, publicó en la Gaceta Oficial de la Ciudad de México el “<strong><i>Aviso por el que se da a conocer el Acuerdo por el que se suprimen, modifican
            y crean los Sistemas de Datos Personales, que detentan las diversas unidades administrativas de la Secretaría de Administración y
            Finanzas</i></strong>”,el 4 de noviembre de 2019.<br><br>

