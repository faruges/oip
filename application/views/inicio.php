        </div>

      <p>
      <br>
          Cualquier información concerniente a una persona física identificada o identificable, es decir, cuando su identidad pueda
          determinarse directa o indirectamente a través de cualquier información como puede ser nombre, número de identificación,
          datos de localización, identificador en línea o uno o varios elementos de la identidad física, fisiológica, genética,
          psíquica, patrimonial, económica, cultural o social de la persona.
